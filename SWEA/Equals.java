package ssafy.string;

public class Equals {

    public static void main(String[] args) {

        String str1 = "Hi";
        String str2 = "Hello";

        if (str1.equals((str2))) {
            System.out.println("Equals");
        } else if (!str1.equals(str2)) {
            System.out.println("Not Equals");
        }
    }
}
